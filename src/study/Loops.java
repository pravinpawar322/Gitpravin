package study;

public class Loops {
public static void main(String[]args) {
	int a =0;
	while(a<=10) {
		System.out.print(a+" ");
		a++;
	}
	System.out.println();
	int b=0;
	do{
		System.out.print(b+" ");
		b++;
	}while(b<10);
	
	System.out.println();
	int c=0;
	for(c=0;c<=10;c++) {
		System.out.print(c+" ");
	}
	
}
}
